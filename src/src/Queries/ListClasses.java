    public void SetData() throws SQLException{
         jTable1.setModel(new javax.swing.table.DefaultTableModel(
            new Object [][] {
                //starting with empty rows
            },
            new String [] {
                "Class", "Dept#", "Course#", "Sect#", "Year", "Semester", "Limit", "Class Size", "Faculty#"
            }
        ));
         
          while (rset.next()) {
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.addRow(new Object[]
            {
                rset.getString(1), rset.getString(2), rset.getString(3),
                rset.getString(4), rset.getString(5), rset.getString(6),
                rset.getString(7), rset.getString(8), rset.getString(9)
            }
            );
          }
        
       
    }
     
     public void LoadData() throws SQLException{
        Connections cn = new Connections();
        OracleDataSource ds = new oracle.jdbc.pool.OracleDataSource();
        ds.setURL(cn.getConnectionString());
        Connection conn = ds.getConnection(cn.getUserName(), cn.getPassword());

      //Prepare to call stored procedure:
        Procedures p = new Procedures();
        CallableStatement cs = conn.prepareCall(p.Third());
        
	   //register the out parameter (the first parameter)
        cs.registerOutParameter(1, OracleTypes.CURSOR);
        
        //2nd parameter
        cs.setString(2, jTextField1.getText());
        
        
        // execute and retrieve the result set
        
        try{
            cs.execute();
            rset = (ResultSet)cs.getObject(1);
            //calling setData
            SetData();
        }
        catch(SQLException ex){
            
            if(ex.getErrorCode( )==20001)
                JOptionPane.showMessageDialog(null,"The student has not taken any course.");
            if(ex.getErrorCode( )==20002)
                JOptionPane.showMessageDialog(null,"The B# is invalid.");
            if(ex.getErrorCode( )==20034)
                JOptionPane.showMessageDialog(null,"Other error Occured");
        }
        
       // System.out.println("my error----"+rset);
    }

